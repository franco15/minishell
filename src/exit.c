#include "minishell.h"

void	ft_exit(char **s)
{
	(void)s;
	int	i;

	i = 0;
	// ft_arrdel((void**)s);
	while (s[i] != 0)
		ft_memdel((void**)&s[i++]);
	// ft_memdel((void**)s);
	exit(3);
}
